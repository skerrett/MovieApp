package com.example.movieapp;

public class Post {

    private String name;

    private int releaseYear;

    private String genre;

    private int stars;

    private int runtime;

    public String getName() {
        return name;
    }

    public int getReleaseYear() {
        return releaseYear;
    }

    public String getGenre() {
        return genre;
    }

    public int getStars() {
        return stars;
    }

    public int getRuntime() {
        return runtime;
    }
}
